import fs from "fs";
import os from "os";
import path from "path";
import crypto from "crypto";

function parseArgs(argv) {
  const args = {
    autoConnect: true,
    clear: false,
  };

  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--clear") {
      args.clear = true;
    } else if (arg === "--server-url") {
      args.serverUrl = argv[++i];
    } else if (arg === "--device-id") {
      throw new Error("--device-id is no longer supported; pass --pairing-code instead");
    } else if (arg === "--refresh-token") {
      throw new Error("--refresh-token is no longer supported; pass --pairing-code instead");
    } else if (arg === "--pairing-code") {
      args.pairingCode = argv[++i];
    } else if (arg === "--auto-connect") {
      const value = argv[++i];
      args.autoConnect = value !== "false";
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  if (!args.clear) {
    if (!args.serverUrl) throw new Error("Missing --server-url");
    if (!args.pairingCode) throw new Error("Missing --pairing-code");
  }
  return args;
}

const args = parseArgs(process.argv.slice(2));
const targetPath = path.resolve("workspace/apps/mobile/src/generated/devBootstrap.ts");
const backupPath = path.join(
  os.tmpdir(),
  `natstack-mobile-dev-bootstrap-${crypto
    .createHash("sha256")
    .update(process.cwd())
    .digest("hex")
    .slice(0, 16)}.ts`
);

const contents = args.clear ? `/**
 * Development bootstrap for the mobile workspace app.
 *
 * Generated by scripts/write-mobile-dev-bootstrap.mjs.
 * Regenerate this file when the local gateway URL or device credential changes.
 */

export interface DevBootstrapConfig {
  serverUrl: string;
  pairingCode: string;
  serverId?: string;
  workspaceId?: string;
  autoConnect?: boolean;
}

export const devBootstrapConfig: DevBootstrapConfig | null = null;
` : `/**
 * Development bootstrap for the mobile workspace app.
 *
 * Generated by scripts/write-mobile-dev-bootstrap.mjs.
 * Regenerate this file when the local gateway URL or device credential changes.
 */

export interface DevBootstrapConfig {
  serverUrl: string;
  pairingCode: string;
  serverId?: string;
  workspaceId?: string;
  autoConnect?: boolean;
}

export const devBootstrapConfig: DevBootstrapConfig | null = {
  serverUrl: ${JSON.stringify(args.serverUrl)},
  pairingCode: ${JSON.stringify(args.pairingCode)},
  autoConnect: ${args.autoConnect ? "true" : "false"},
};
`;

if (args.clear && fs.existsSync(backupPath)) {
  fs.writeFileSync(targetPath, fs.readFileSync(backupPath, "utf8"), "utf8");
  fs.rmSync(backupPath, { force: true });
  console.log(`Restored ${targetPath}`);
} else {
  if (!args.clear && !fs.existsSync(backupPath)) {
    fs.writeFileSync(backupPath, fs.readFileSync(targetPath, "utf8"), "utf8");
  }
  fs.writeFileSync(targetPath, contents, "utf8");
  console.log(`Wrote ${targetPath}`);
}
